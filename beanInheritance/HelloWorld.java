package com.spring.beanInheritance;

public class HelloWorld {
	private String worldMessage1;
	private String worldMessage2;

	/**
	 * @return the worldMessage1
	 */
	public String getWorldMessage1() {
		return worldMessage1;
	}

	/**
	 * @return the worldMessage2
	 */
	public String getWorldMessage2() {
		return worldMessage2;
	}

	/**
	 * @param worldMessage1
	 *            the worldMessage1 to set
	 */
	public void setWorldMessage1(String worldMessage1) {
		this.worldMessage1 = worldMessage1;
	}

	/**
	 * @param worldMessage2
	 *            the worldMessage2 to set
	 */
	public void setWorldMessage2(String worldMessage2) {
		this.worldMessage2 = worldMessage2;
	}

}
